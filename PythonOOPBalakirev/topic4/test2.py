class Calculator:
    @staticmethod
    def plusovanie(num1, num2):
        return num1 + num2

    @staticmethod
    def minusovanie(num1, num2):
        return num1 - num2

    @staticmethod
    def umnozhenie(num1, num2):
        return num1 * num2

    @staticmethod
    def delenie(num1, num2):
        return num1 / num2
